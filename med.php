</div>
<br><br><br><br><br><br>

<div class="container">
	<div class="row">
		<div class="col-sm-12">
		<p>Med je viskózna sladká a lepkavá tekutina, ktorú vytvárajú niektoré druhy hmyzu (hlavne včely) zberom a zahusťovaním sladkých štiav – predovšetkým nektáru kvetov. Pre včelstvo je med zásobou energeticky bohatej potravy. Počas roka silné včelstvo vyrobí a z väčšej časti i spotrebuje rádovo stovky kilogramov medu. Na prečkanie zimy včelstvu stačí približne 1 kg na mesiac.<br><br>
Med má zvláštnu chuť, pre ktorú mu niektorí ľudia dávajú prednosť pred cukrom a inými sladidlami. Má vhodné chemické vlastnosti pre pečenie. Kvapalný med sa nekazí. Vďaka vysokému obsahu cukru ničí baktérie procesom nazývaným osmolýza. Nemôžu sa na ňom uchytiť ani kvasinky zo vzduchu, pretože obsah vody je príliš nízky. Prírodný neupravený med má obsah vody asi 14 % – 18 %. Ak je obsah vody pod 18 %, nemôže sa v mede množiť prakticky žiadny organizmus.<br><br>
Pri dlhodobom skladovaní pri izbovej (alebo vyššej) teplote sa môže vďaka sedimentácii cukrov na povrchu medu v nádobe vytvoriť tenká redšia vrstvička, v ktorej sa rozmnožujú osmofilné druhy kvasiniek (kvasia koncentrované roztoky cukrov – nad 50 %), ktoré sú v mede prirodzene obsiahnuté. Takéto kvasenie ovplyvňuje predovšetkým senzorické, ale nie nutričné vlastnosti medu.<br><br>
Rozborom peľov a výtrusov (rastlín) v neupravenom mede (melisopalynológia) sa dajú určiť kvety, z ktorých bol zozbieraný. Pretože včely prenášajú elektrostatický náboj a môžu priťahovať iné častice, môžu sa niektoré techniky melisopalynológie použiť v oblasti environmentálnych štúdií rádioaktívnych častíc, prachu, alebo časticového znečistenia.</p>
