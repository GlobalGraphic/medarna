		<p>Stať sa včelárom bolo mojím snom od útleho detstva. Už ako 14-ročný chlapec som sa chcel starať o včely. No vyrastal som v meste, kde som tento svoj sen nemohol dlho uskutočniť. Aj napriek tomu som sa ho však nevzdal a dnes to neľutujem. Dlhý čas som ovládal asi všetku možnú teóriu o tom, ako sa o včely starať, ale nevedel som nájsť dostatok odvahy začať túto teóriu praktizovať. Potom sa však na mňa usmialo šťastie.<br><br>
Pred pár rokmi som postavil dom na vidieku a môj sen sa začal napĺňať. V záhrade mojich svokrovcov sa objavil roj včiel a ja som hneď vedel, že bude môj. Pomohol mi aj môj svokor, keď mi kúpil štyri kompletné úle značky Langstroth. To ma naštartovalo a som mu za to nesmierne vďačný. Rovnako patrí moja vďaka aj miestnym včelárom, ktorí mi tiež pomohli. Písal sa vtedy rok 2016.<br><br>
Dnes mám už 40 rokov a starám sa o 10 včelích rodín. Mojím plánom je aj naďalej ich počet rozširovať. Prečo? Pretože to mám zjavne v krvi a robí mi to radosť. Aj môj pradedo bol totiž včelár. Narodil sa v r. 1905 a žil v Kosihovciach v okrese Veľký Krtíš, kde sa staral o 30 včelích rodín. Ja bývam v Slepčanoch v okrese Zlaté Moravce.
Všetky včelie produkty mojich včiel pochádzajú z najčistejších lesov a lúk regiónu Požitavie - Širočina. Aj to bol jeden z aspektov môjho úspechu. Na medzinárodnej súťaži včelárov v mestečku Mugla v Turecku som totiž v roku 2018 vyhral 3. miesto s medom z facélie. Facélia je rastlina pôvodom z Kalifornie. Na Slovensku je známa najmä medzi včelármi pre bohatú produkciu peľu a nektáru.<br><br>
No včelárstvo nie je len mojím koníčkom, ale hlavne vášňou. Aj keď som mal z počiatku obavy začať, dnes viem, že som urobil dobre, keď som sa rozhodol splniť svoj detský sen. Som si istý, že toto rozhodnutie určite nikdy nebudem ľutovať. A ktovie, možno sa táto moja záľuba prenesie aj na mojich potomkov. 
