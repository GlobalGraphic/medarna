		<div class="col-sm-12">
			<p>Predajcovia zdravej výživy propagujú propolis ako prírodné liečivo s priaznivými účinkami na ľudské zdravie. Ľudoví liečitelia ho často používajú na zmiernenie stavov ako sú zápaly, ochorenia vírusového pôvodu, vredy, ľahké popáleniny a obareniny. Údajne tiež priaznivo pôsobí na srdce a znižuje riziko šedého zákalu. Starí včelári odporúčajú vložiť kúsok propolisu do úst ako liek na boľavé hrdlo. Taktieź je možné zakúpiť propolisové pastilky. Hoci sa odporúča pri liečbe alergií, u ľudí citlivých na včely a včelie produkty môže spôsobiť silnú alergickú reakciu.<br><br>
Niektoré z týchto tvrdení o účinkoch propolisu sú v súčasnosti objektom klinického výskumu a niekoľko študií sa objavilo v odbornej literatúre. Pretože chemické zloženie propolisu sa mení podľa ročného obdobia, druhu včiel a zemepisnej polohy, s výsledkami týchto výskumov treba zaobchádzať opatrne. Propolis pôsobí na imunitný systém, v závislosti od zloženia môže mať silné antibiotické a antifungálne účinky a štúdie naznačujú, že skutočne môže byť účinný pri liečbe popálenín. Je tiež objektom stomatologického výskumu, pretože existujú dôkazy, že vďaka svojim antibakteriálnym vlastnostiam aktívne chráni pred zubným kazom a inými ochoreniami ústnej dutiny.</p>
