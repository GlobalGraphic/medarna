  <h2 style="font-size: 58px;">Zloženie</h2> 
</div>
<br><br><br>

<div class="container">
	<div class="row">
		<div class="col-sm-12">
			<p>Zloženie propolisu je v každom úli, v každej oblasti a v každom ročnom období iné. Jeho farba zavisí od zdroja živice. Väčšinou je tmavohnedý, ale môže sa vyskytovať aj v odtieňoch zelenej, červenej, čiernej a bielej. Vlastnosti propolisu závisia od rastlinného zdroja a včelár ich nemôže ovplyvniť. To môže byť dôvodom, prečo sa propolisu pripisuje toľko rôznorodých liečivých účinkov, a znemožniť opakovanie vedeckých štúdií, ktoré sa výskumom týchto účinkov zaoberajú. Aj vzorky propolisu získané z jedného včelstva sa môžu líšiť a kontrolované klinické testy sú preto takmer nemožné.<br><br>
Zdroj propolisu sa mení so zemepisnou šírkou. V miernom podnebí včely zbierajú živicu zo stromov, hlavne topoľov a do nižšej miery ihličnatých stromov. Úlohou propolisu v prírode je zaceliť rany a chrániť stromy pred baktériami, hubami a hmyzom. V tropických oblastiach včely zbierajú propolis z kvetov, hlavne klúzií, ktoré ho používajú na prilákania opeľovačov. Chemické zloženie propolisu z miernych a tropických oblastí sa líši. Typický propolis má asi 50 zložiek, 50 % tvoria živice a rastlinné balzamy, 30 % vosky, 10 % esenciálne oleje a 5 % peľ.<br><br>
Chuť propolisu je horká a viacmenej štipľavá. Vcelku sa dá povedať, že jeho chuť, najmä v alkoholivých preparátoch, je typická.</p>
